public class ApiResponse {
    private String base_code; // Código de la moneda base
    private String target_code; // Código de la moneda objetivo
    private double conversion_rate; // Tasa de conversión
    private double conversion_result; // Resultado de la conversión (si está disponible)

    // Getters y Setters
    public String getBase_code() {
        return base_code;
    }

    public void setBase_code(String base_code) {
        this.base_code = base_code;
    }

    public String getTarget_code() {
        return target_code;
    }

    public void setTarget_code(String target_code) {
        this.target_code = target_code;
    }

    public double getConversion_rate() {
        return conversion_rate;
    }

    public void setConversion_rate(double conversion_rate) {
        this.conversion_rate = conversion_rate;
    }

    public double getConversion_result() {
        return conversion_result;
    }

    public void setConversion_result(double conversion_result) {
        this.conversion_result = conversion_result;
    }
}